#ifndef _LaplaceRandomVariable_h
#define _LaplaceRandomVariable_h

#include "AbstractRandomVariable.h"

class LaplaceRandomVariable : public AbstractRandomVariable {
public:
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Constructor(s) / Destructor
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    LaplaceRandomVariable(double Location, double Scale);
    ~LaplaceRandomVariable();

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Getter(s) and Setter(s):
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    // Get the scale of the laplace distribution
    double GetScale() { return m_Scale; }

    // Get the density estimation
    inline double GetDensity() const;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Other Method(s) :
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    // Sample to update the current state
    void Sample();


private:
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Attribute(s) :
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    // Location of the laplace distribution
    double m_Location;

    // Scale of the laplace distribution
    double m_Scale;

};


#endif //_LaplaceRandomVariable_h
