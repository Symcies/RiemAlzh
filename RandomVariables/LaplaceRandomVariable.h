#ifndef _LaplaceRandomVariable_h
#define _LaplaceRandomVariable_h


#include "AbstractRandomVariable.h"

class LaplaceRandomVariable : public AbstractRandomVariable{
public:
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Constructor(s) / Destructor :
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    LaplaceRandomVariable(double Location, double Scale);
    ~LaplaceRandomVariable();

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Method(s) :
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    /// Draw a sample
    virtual double Sample();

    /// Compute the likelihood given a current state
    virtual double Likelihood(double X);
    
    /// Compute the log likelihood given a current state
    virtual double LogLikelihood(double X);

    // TODO : Check if needed
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // DEBUGGING METHOD BUT MIGHT BE CONSIDERED IN PRODUCTION
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    /// Print the parameters
    virtual void PrintParameters();


protected:
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // Attribute(s)
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    /// Location of the Laplace distribution
    double m_Location;

    /// Scale of the Laplace distribution
    double m_Scale;
};


#endif //_LaplaceRandomVariable_h
