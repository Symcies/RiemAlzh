double
AbstractSampler
::DecreasingStepSize(int Iteration, int NoMemoryTime) 
{
    double Epsilon = std::max(1, Iteration - NoMemoryTime);
    double Lambda = 0.51; // Lambda should belong to ]1/2 ; 1]
    return 1.0 / pow(Epsilon, Lambda);
}
